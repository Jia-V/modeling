dx(12) = (b6*k4*b7*x(9)*x(11)*(b3*x(4)*x(6)/d3))/(d6*d7+d6*k4+b7*k4*(b3*x(4)*x(6)/d3))-(b8*k6*x(12)*x(13))/(d8+k6);
end