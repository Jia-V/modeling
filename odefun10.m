function dx = odefun10(t,x)
dx = zeros(13,1);% 列向量
%参数值
b1=1e-6;
d1=1;
b2=1e-3;
d2=0.1;
k1=3.6e+3;
x2=1;
b3=5e-4;
d3=0.1;
b4=5e-06;
b5=5e-06;
b6=2.6e-04;
b7=5e-06;
b8=1.7e-06;
